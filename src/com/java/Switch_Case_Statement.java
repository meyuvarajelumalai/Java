package com.java;

public class Switch_Case_Statement {
	public static void main(String[] args) {
		int i = 3;
		switch (i) {
		case 1:
			System.out.println("10");
			break;
		case 2:
			System.out.println("20");
			break;
		case 3:
			System.out.println("40");
			break;
		default:
			System.out.println("Invalid Number");
			break;
		case 10:
			System.out.println("60");
			break;

		}
	}

}
